package com.theincgi.gplua.cl;

public class LuaCompileException extends RuntimeException {

	public LuaCompileException() {
		super();
	}

	public LuaCompileException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LuaCompileException(String message, Throwable cause) {
		super(message, cause);
	}

	public LuaCompileException(String message) {
		super(message);
	}

	public LuaCompileException(Throwable cause) {
		super(cause);
	}
	
	
	
	
}
